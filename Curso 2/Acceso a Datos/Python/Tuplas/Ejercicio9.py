tupla1 = (1, 2, 3, 4, 5)
tupla2 = (3, 4, 5, 6, 7)

interseccion = tuple(set(tupla1) & set(tupla2))
diferencia = tuple(set(tupla1) ^ set(tupla2))

print("Intersección:", interseccion)
print("Diferencia:", diferencia)
