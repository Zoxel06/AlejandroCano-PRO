public class Motor {

    private int litros, cv;

    public Motor(){
    }

    public Motor(int cv){
        this.cv = cv;
    }

    public int getLitros() {
        return litros;
    }

    public void setLitros(int litros) {
        this.litros = litros;
    }

    public int getCv() {
        return cv;
    }

    public void setCv(int cv) {
        this.cv = cv;
    }

}
